-- Crea el perfil al registrarse un usuario (lo dispara el trigger en auth.users)
create or replace function public.crear_perfil()
returns trigger language plpgsql security definer set search_path to 'public'
as $$
begin
  insert into public.perfiles(id, username, referido_por)
  values (new.id, new.raw_user_meta_data->>'username', new.raw_user_meta_data->>'referido_por');
  return new;
end; $$;

-- Rate limiting: identifica por usuario (con sesión) o IP (anónimo); admins exentos
create or replace function public.check_rate_limit(p_accion text, p_max int, p_minutos int)
returns void language plpgsql security definer set search_path to 'public'
as $$
declare v_ident text; v_ip text; v_count int;
begin
  if auth.uid() is not null
     and coalesce((select es_admin from perfiles where id = auth.uid()), false) then
    return;  -- los admins no tienen límite
  end if;
  if auth.uid() is not null then
    v_ident := 'u:' || auth.uid()::text;
  else
    v_ip := split_part(coalesce(nullif(current_setting('request.headers', true),'')::json ->> 'x-forwarded-for',''), ',', 1);
    v_ident := 'ip:' || coalesce(nullif(trim(v_ip),''), 'desconocido');
  end if;
  delete from public.rate_limit where ident = v_ident and at < now() - interval '1 hour';
  select count(*) into v_count from public.rate_limit
   where ident = v_ident and accion = p_accion and at > now() - make_interval(mins => p_minutos);
  if v_count >= p_max then
    raise exception 'Vas muy rápido. Espera unos minutos e intenta de nuevo.';
  end if;
  insert into public.rate_limit(ident, accion) values (v_ident, p_accion);
end; $$;

-- Deriva autor/autor_nivel desde auth.uid() (anti-suplantación) + rate limit.
-- Lo usan los triggers BEFORE INSERT de reportes y fotos.
create or replace function public.set_autor_real()
returns trigger language plpgsql security definer set search_path to 'public'
as $$
declare v_user text; v_pts int;
begin
  if tg_table_name = 'reportes' then
    perform public.check_rate_limit('reporte', case when auth.uid() is not null then 3 else 2 end, 15);
  elsif tg_table_name = 'fotos' then
    perform public.check_rate_limit('foto', case when auth.uid() is not null then 3 else 2 end, 15);
  end if;
  if auth.uid() is null then
    new.autor := null; new.autor_nivel := null;
  else
    select username, puntos into v_user, v_pts from public.perfiles where id = auth.uid();
    new.autor := v_user;
    new.autor_nivel := case when v_pts>=1000 then 4 when v_pts>=400 then 3
                            when v_pts>=150 then 2 when v_pts>=50 then 1 else 0 end;
  end if;
  return new;
end; $$;

-- Elegir/cambiar username (valida formato y unicidad)
create or replace function public.elegir_username(p_username text)
returns text language plpgsql security definer set search_path to 'public'
as $$
declare v_uid uuid := auth.uid();
begin
  if v_uid is null then return 'no_auth'; end if;
  if p_username !~ '^[A-Za-z0-9_]{3,20}$' then return 'formato'; end if;
  if exists(select 1 from public.perfiles where lower(username)=lower(p_username) and id<>v_uid)
     then return 'taken'; end if;
  update public.perfiles set username=p_username where id=v_uid;
  if not found then insert into public.perfiles(id,username) values(v_uid,p_username); end if;
  return 'ok';
end; $$;

-- Suma puntos al usuario actual (monto fijo, ignora cualquier n del cliente)
create or replace function public.sumar_mis_puntos(n integer)
returns integer language plpgsql security definer set search_path to 'public'
as $$
declare nuevo int;
begin
  if auth.uid() is null then return null; end if;
  update perfiles set puntos = puntos + 10 where id = auth.uid() returning puntos into nuevo;
  return nuevo;
end; $$;

-- Premia al referidor, una sola vez, validando el referido_por real del que llama
create or replace function public.puntos_por_referido(ref text)
returns void language plpgsql security definer set search_path to 'public'
as $$
declare v_ya boolean; v_ref text;
begin
  if auth.uid() is null then return; end if;
  select referido_premiado, referido_por into v_ya, v_ref from perfiles where id = auth.uid();
  if v_ya is true or v_ref is null or lower(v_ref) <> lower(ref) then return; end if;
  update perfiles set puntos = puntos + 20 where lower(username) = lower(ref);
  update perfiles set referido_premiado = true where id = auth.uid();
end; $$;

-- Registra/incrementa un lugar mencionado (y recuerda su ubicación si viene)
create or replace function public.registrar_lugar(p_region text, p_tipo text, p_nombre text, p_clave text,
  p_lat double precision default null, p_lng double precision default null)
returns integer language plpgsql security definer set search_path to 'public'
as $$
declare total int;
begin
  insert into lugares (region, tipo, nombre, clave, lat, lng)
  values (p_region, p_tipo, p_nombre, p_clave, p_lat, p_lng)
  on conflict (region, tipo, clave)
  do update set menciones = lugares.menciones + 1,
                lat = coalesce(excluded.lat, lugares.lat),
                lng = coalesce(excluded.lng, lugares.lng)
  returning menciones into total;
  return total;
end; $$;

-- Votar un reporte (sube votos_ok/votos_no)
create or replace function public.votar(reporte uuid, ok boolean)
returns void language sql security definer set search_path to 'public'
as $$
  update reportes
     set votos_ok = votos_ok + (case when ok then 1 else 0 end),
         votos_no = votos_no + (case when ok then 0 else 1 end)
   where id = reporte;
$$;

-- Comentar (deriva autor de auth.uid(), valida y aplica rate limit)
create or replace function public.comentar(p_tipo text, p_ref uuid, p_texto text,
  p_autor text default null, p_autor_nivel integer default null)
returns void language plpgsql security definer set search_path to 'public'
as $$
declare v_user text; v_pts int;
begin
  if p_tipo not in ('reporte','foto') then raise exception 'tipo inválido'; end if;
  if p_texto is null or char_length(trim(p_texto)) < 2 or char_length(p_texto) > 100 then
    raise exception 'texto inválido (2 a 100 caracteres)'; end if;
  perform public.check_rate_limit('comentario', case when auth.uid() is not null then 6 else 3 end, 15);
  select username, puntos into v_user, v_pts from public.perfiles where id = auth.uid();
  insert into comentarios (tipo, ref_id, texto, autor, autor_nivel)
  values (p_tipo, p_ref, trim(p_texto), v_user,
          case when v_user is null then null
               when v_pts>=1000 then 4 when v_pts>=400 then 3
               when v_pts>=150 then 2 when v_pts>=50 then 1 else 0 end);
  if p_tipo = 'reporte' then
    update reportes set comentarios_n = comentarios_n + 1 where id = p_ref;
  else
    update fotos set comentarios_n = comentarios_n + 1 where id = p_ref;
  end if;
end; $$;

