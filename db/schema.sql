-- Borrar mi propio reporte (devuelve los storage_path de sus fotos para limpiar)
create or replace function public.borrar_mi_reporte(rid uuid)
returns text[] language plpgsql security definer set search_path to 'public'
as $$
declare v_yo text; v_paths text[];
begin
  select p.username into v_yo from public.perfiles p where p.id = auth.uid();
  select array_agg(f.storage_path) into v_paths from public.fotos f where f.reporte_id = rid;
  delete from public.reportes r where r.id = rid and r.autor is not null and r.autor = v_yo;
  if not found then return null; end if;
  delete from public.fotos f where f.reporte_id = rid;
  return v_paths;
end; $$;

-- Borrar mi propia foto (devuelve el storage_path para limpiar)
create or replace function public.borrar_mi_foto(foto_id uuid)
returns text language plpgsql security definer set search_path to 'public'
as $$
declare v_yo text; v_path text;
begin
  select p.username into v_yo from public.perfiles p where p.id = auth.uid();
  delete from public.fotos f where f.id = foto_id and f.autor is not null and f.autor = v_yo
   returning f.storage_path into v_path;
  return v_path;
end; $$;

-- Usuario pide permiso para subir fotos (lo revisa un admin)
create or replace function public.solicitar_foto()
returns void language plpgsql security definer set search_path to 'public'
as $$
begin
  update public.perfiles set foto_solicitada = true
   where id = auth.uid() and coalesce(posts_ilimitados,false) = false;
end; $$;

-- Publicar foto en un reporte (solo usuarios con posts_ilimitados)
create or replace function public.publicar_foto_reporte(p_path text, p_label text, p_reporte uuid, p_region text, p_autor text, p_nivel integer)
returns void language plpgsql security definer set search_path to 'public'
as $$
begin
  if not exists (select 1 from public.perfiles where id = auth.uid() and coalesce(posts_ilimitados,false) = true) then
    raise exception 'Sin permiso para publicar fotos';
  end if;
  insert into public.fotos (region, storage_path, estado, aprobada_at, autor, autor_nivel, reporte_id, reporte_label)
  values (p_region, p_path, 'aprobada', now(), p_autor, p_nivel, p_reporte, p_label);
  update public.reportes set foto_path = p_path where id = p_reporte;
end; $$;

-- ---- Funciones de admin (validan es_admin por dentro) ----

create or replace function public.aprobar_foto(foto_id uuid)
returns void language plpgsql security definer set search_path to 'public'
as $$
begin
  if not coalesce((select es_admin from perfiles where id = auth.uid()), false) then
    raise exception 'No autorizado';
  end if;
  update fotos set estado = 'aprobada', aprobada_at = now()
  where id = foto_id and estado = 'pendiente';
end $$;

create or replace function public.rechazar_foto(foto_id uuid)
returns void language plpgsql security definer set search_path to 'public'
as $$
begin
  if not coalesce((select es_admin from perfiles where id = auth.uid()), false) then
    raise exception 'No autorizado';
  end if;
  delete from fotos where id = foto_id;  -- el archivo lo borra la app vía Storage
end $$;

create or replace function public.fotos_pendientes()
returns setof public.fotos language plpgsql security definer set search_path to 'public'
as $$
begin
  if not coalesce((select es_admin from perfiles where id = auth.uid()), false) then
    raise exception 'No autorizado';
  end if;
  return query select * from fotos where estado = 'pendiente' order by created_at asc;
end $$;

create or replace function public.aprobar_foto_usuario(uid uuid)
returns void language plpgsql security definer set search_path to 'public'
as $$
begin
  if not exists (select 1 from public.perfiles p where p.id = auth.uid() and p.es_admin) then
    raise exception 'No autorizado';
  end if;
  update public.perfiles p set posts_ilimitados = true, foto_solicitada = false where p.id = uid;
end; $$;

create or replace function public.solicitudes_foto()
returns table(id uuid, username text, puntos integer) language plpgsql security definer set search_path to 'public'
as $$
begin
  if not exists (select 1 from public.perfiles p where p.id = auth.uid() and p.es_admin) then
    raise exception 'No autorizado';
  end if;
  return query
    select p.id, p.username, p.puntos from public.perfiles p
    where p.foto_solicitada = true and coalesce(p.posts_ilimitados,false) = false
    order by p.username;
end; $$;

-- Safety: activa RLS automáticamente en tablas nuevas de public
create or replace function public.rls_auto_enable()
returns event_trigger language plpgsql security definer set search_path to 'pg_catalog'
as $$
declare cmd record;
begin
  for cmd in
    select * from pg_event_trigger_ddl_commands()
    where command_tag in ('CREATE TABLE', 'CREATE TABLE AS', 'SELECT INTO')
      and object_type in ('table','partitioned table')
  loop
    if cmd.schema_name is not null and cmd.schema_name = 'public' then
      begin
        execute format('alter table if exists %s enable row level security', cmd.object_identity);
      exception when others then null;
      end;
    end if;
  end loop;
end; $$;

